/** Public list of (family-label → ordered mode tonalities) used by the
 *  41-EDO picker override in jiTonalityFamilies.ts.  Each family lists
 *  its parent first then six rotated modes. */
export const FORTY_ONE_EDO_TONALITY_FAMILIES: { parent: string; tonalities: string[] }[] = [];
export const FIFTY_THREE_EDO_TONALITY_FAMILIES: { parent: string; tonalities: string[] }[] = [];

// Generate 6 modes per parent and append them to JI_SCALES.  De-dup
// across the 41 / 53 family lists — many parents are shared, so a
// single parent's modes only need generating once.  Mode names are
// computed by buildModeNameFromCents from the rotation's actual
// cents pattern, so each mode reads as e.g. "Diatonic Supermajor
// Dorian S2 #5 S6" (Western mode + alterations) instead of the
// previous flat "<Parent> <Greek mode>" naming that didn't capture
// the rotation's specific interval colour.  spec.modeNames is now
// only consulted as a fallback for the harmonic-minor taxonomy
// (since e.g. "Phrygian Dominant" isn't reproducible from the
// alteration algorithm alone — it's a named scale).
const generatedModeNames = new Set<string>();
function expandFamily(spec: FortyOneEdoFamilySpec, intoOut: { parent: string; tonalities: string[] }[]) {
  const parentSpec = JI_SCALES.find(s => s.name === spec.parent);
  if (!parentSpec) return;
  const tonalities: string[] = [spec.parent];
  for (let r = 1; r < 7; r++) {
    const rotated = rotateScaleSteps(parentSpec.steps, r);
    const rotatedCents = rotated.map(([, c]) => c);
    const calculatedName = buildModeNameFromCents(rotatedCents);
    const modeName = `${spec.parent} ${calculatedName}`;
    if (!generatedModeNames.has(modeName)) {
      JI_SCALES.push({ name: modeName, steps: rotated });
      generatedModeNames.add(modeName);
    }
    tonalities.push(modeName);
  }
  intoOut.push({ parent: spec.parent, tonalities });
}
for (const spec of FORTY_ONE_EDO_FAMILIES) expandFamily(spec, FORTY_ONE_EDO_TONALITY_FAMILIES);
for (const spec of FIFTY_THREE_EDO_FAMILIES) expandFamily(spec, FIFTY_THREE_EDO_TONALITY_FAMILIES);

// ── Build per-EDO ScaleFamilyMaps ────────────────────────────────────────
//
// edoData's getModeDegreeMap looks up scales as
//   maps[scaleFam]?.[modeName]
// so we group every JI scale under one synthetic family per EDO ("JI
// Family") and key by the scale's display name.  The bank/picker code in
// jiTonalityFamilies.ts surfaces them through the limit groups; the
// underlying lookup just needs the map.

const JI_FAMILY_NAME = "JI Family";

function centsToStep(cents: number, edo: number): number {
  return Math.round((cents / 1200) * edo);
}

function buildJiScalesForEdo(edo: number): ScaleFamilyMap {
  const fam: Record<string, ModeMap> = {};
  for (const scale of JI_SCALES) {
    const map: ModeMap = {};
    for (const [degLabel, cents] of scale.steps) {
      map[degLabel] = centsToStep(cents, edo);
    }
    fam[scale.name] = map;
  }
  return { [JI_FAMILY_NAME]: fam };
}

// ── Module-load registration ────────────────────────────────────────────
// Side-effect: hooks the JI families into edoData's pattern-map cache so
// getModeDegreeMap(41, "JI Family", "Pythagorean Ionian") returns the
// degree map without any further wiring.  Mirrors the 31-EDO xen
// registration in musicTheory.ts.

registerXenPatternMaps(41, buildJiScalesForEdo(41));
registerXenPatternMaps(53, buildJiScalesForEdo(53));

// Cent-value side-channel for the chord analyzer — avoids a circular
// import by letting jiChordAnalysis.ts pull the underlying cents
// without re-importing JI_SCALES.
for (const scale of JI_SCALES) {
  registerScaleCents(scale.name, scale.steps.map(([, c]) => c));
}

