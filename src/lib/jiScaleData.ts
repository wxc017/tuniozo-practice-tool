//
// Step values for the 19 curated JI scales the Tonal Audiation tabs
// expose under Pythagorean and Schismatic temperaments.  Each scale's
// degree → step map is computed from the underlying just-intonation
// ratios converted to the nearest integer step in 41-EDO and 53-EDO.
//
// The names here match the public tonality identifiers in
// jiTonalityFamilies.ts.  A module-load side-effect registers these maps
// with edoData.ts so getModeDegreeMap() returns them, mirroring how the
